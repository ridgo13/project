const form = document.querySelector('.form');
var height = document.querySelector('#height');
var weight = document.querySelector('#weight');
const bmiOutput = document.querySelector('.bmii');
const remarkkOutput = document.querySelector('.remark');

form.onsubmit = function(e) {
    e.preventDefault();
    const userHeight = parseFloat(height.value);
    const userWeight = parseFloat(weight.value);
    const result = userWeight / (userHeight**2);
    const BMI = result.toFixed(2);

    height.value = '';
    weight.value = '';

    bmiOutput.innerHTML = BMI

    if (BMI < 18.5) {
        remarkkOutput.innerHTML = 'You are underweight!';
    } else if (BMI >= 18.5 || BMI < 25) {
        remarkkOutput.innerHTML = 'You are healthy weight!';
    } else if(BMI >= 25 || BMI < 30) {
        remarkkOutput.innerHTML = 'You are overweight!';
    } else if (BMI >= 30) {
        remarkkOutput.innerHTML = 'Obesity!';
    }
}

